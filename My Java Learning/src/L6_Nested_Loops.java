// Suppose we want to print the pattern
/*
  
  * * * *
  * * * *
  * * * *
  * * * *
  
  
 */
public class L6_Nested_Loops {

	public static void main(String args[]) {
		
		for(int i=1;i<=4;i++) {
			for(int j=1;j<=4;j++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		
	}
}
